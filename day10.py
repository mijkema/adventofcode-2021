def get_amount(char):
    if char == ')': return 3
    if char == ']': return 57
    if char == '}': return 1197
    if char == '>': return 25137


def main(inp, is_real):
    inp = inp.strip().split('\n')
    res = 0
    res2 = []
    for line in inp:
        l = []
        for i, c in enumerate(list(line)):
            if c == '[': l.append('[')
            if c == '{': l.append('{')
            if c == '(': l.append('(')
            if c == '<': l.append('<')
            if c == ']':
                last = l.pop()
                if last != '[':
                    res += get_amount(c)
                    l = []
                    break
            if c == '}':
                last = l.pop()
                if last != '{':
                    res += get_amount(c)
                    l = []
                    break
            if c == ')':
                last = l.pop()
                if last != '(':
                    res += get_amount(c)
                    l = []
                    break
            if c == '>':
                last = l.pop()
                if last != '<':
                    res += get_amount(c)
                    l = []
                    break
        if l:
            sum = 0
            for v in reversed(l):
                sum *= 5
                if v == '(': sum += 1
                if v == '[': sum += 2
                if v == '{': sum += 3
                if v == '<': sum += 4
            res2.append(sum)
    print(res)
    print(sorted(res2)[int(len(res2)/2)])


