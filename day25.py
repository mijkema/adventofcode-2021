def print_grid(grid):
    max_x = len(grid[0])
    max_y = len(grid)
    res = ''
    for x in range(max_x + 1):
        for y in range(max_y + 1):
            res += grid[(x, y)]
        res += '\n'
    print(res)


def move(grid, max_x, max_y):
    # first move to the right
    moved = False
    to_execute = []
    for x in range(max_x):
        for y in range(max_y):
            if grid[x][y] == '>' and grid[x][(y+1) % max_y] == '.':
                to_execute.append([x, y, (y+1) % max_y])
    for x, y, ny in to_execute:
        grid[x][y] = '.'
        grid[x][ny] = '>'
        moved = True
    to_execute.clear()

    for x in range(max_x):
        for y in range(max_y):
            if grid[x][y] == 'v' and grid[(x + 1) % max_x][y] == '.':
                to_execute.append([x, y, (x+1) % max_x])
    for x, y, nx in to_execute:
        grid[x][y] = '.'
        grid[nx][y] = 'v'
        moved = True
    return grid, moved


def main(inp, is_real):
    inp = [list(line) for line in inp.strip().split('\n')]

    max_x, max_y = len(inp), len(inp[0])
    # do steps
    steps = 0
    while True:
        new_grid, moved = move(inp, max_x, max_y)
        if not moved:
            print(f'done after {steps + 1} steps')
            return
        else:
            steps += 1


